# ─────────────────────────────────────────────────────────────────────────────

def main() -> None:
    if not TERMS:
        print("TERMS 리스트가 비어 있습니다.")
        return

    if not DB_PATH.exists():
        print(f"DB 파일을 찾을 수 없습니다: {DB_PATH}")
        return

    now = datetime.now(timezone.utc).strftime("%Y-%m-%dT%H:%M:%S+00:00")
    added_by = 0  # 스크립트로 추가된 항목은 0으로 표시

    conn = sqlite3.connect(DB_PATH)
    conn.row_factory = sqlite3.Row

    added = 0
    skipped = 0

    try:
        for raw in TERMS:
            term = raw.strip()
            if not term:
                continue

            existing = conn.execute(
                "SELECT 1 FROM blocked_terms WHERE guild_id=? AND term=?",
                (GUILD_ID, term),
            ).fetchone()

            if existing:
                print(f"  이미 존재: {term!r}")
                skipped += 1
                continue

            conn.execute(
                """
                INSERT INTO blocked_terms
                    (guild_id, term, severity, category, notes, added_by, added_at)
                VALUES (?, ?, ?, ?, ?, ?, ?)
                """,
                (GUILD_ID, term, max(1, min(3, SEVERITY)), CATEGORY[:64], "", added_by, now),
            )
            print(f"  추가됨: {term!r}")
            added += 1

        conn.commit()
    finally:
        conn.close()

    print(f"\n완료 — 추가: {added}개, 건너뜀: {skipped}개")


if __name__ == "__main__":
    main()
